# 進制轉換
'''
轉2進制 bin()，0b開頭: eg. 0b10000==16
轉8進制 oct()，0o開頭: eg. 0o20==16
轉16進制 hex()，0x開頭: eg. 0x10==16

16進制算法: xy == 16*x+y，eg. 0x10 == 1*16+0 == 16，0x1E == 1*16+14 == 30
'''

# 強制資料型態轉換
'''
Python為弱型別語言，所以可以隨意做資料型態轉換

int()  -->  強制轉換為整數
float()  -->  強制轉換為浮點數
'''

